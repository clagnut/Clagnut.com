<h2 class="fn"><span class="given-name">Richard</span> <span class="family-name">Rutter</span></h2>

<p class="adr"><span class="opener">I’m a web site designer</span> living in <span class="locality">Brighton</span>, <span class="country-name">England</span>, with a fabulous wife, beautiful daughter, baby son and a rather naughty <a href="http://www.flickr.com/photos/clagnut/sets/72157606288596458/">dog</a>. I am co-founder of <a href="http://clearleft.com/is/richard-rutter" rel="me">Clearleft</a> and <a href="http://fontdeck.com">Fontdeck</a>. I started designing websites way back in 1994, and since founding Clearleft with <a href="http://clearleft.com/is/andy-budd">Andy Budd</a> and <a href="http://clearleft.com/is/jeremy-keith">Jeremy Keith</a>, I’m pleased to say I’ve retained my role as a user experience designer, working with clients and their customers to translate their ideas into new and improved websites and applications.</p>

<figure class='inline'><img src="/images/Richard-Rutter-600x900.jpg" alt="Photo of Richard Rutter by Jack Barnes" /><figcaption>Photo by Jack Barnes</figcaption></figure>

<p>I began my career designing the <abbr>UX</abbr> of web sites for Barclaycard, as well as numerous dot com start ups. I moved on to become the user experience lead at Multimap, Europe’s most popular mapping site, which was acquired by Microsoft.</p>
<p>At Clearleft my work includes redesigning the Riverford Organics web site, creating a new e-commerce presence for Pearson Education, designing a radical new location-based search for Gumtree, redesigning a video-on-demand service for a major broadcaster and implementing massive change in the publication habits of a London council.</p>
<p>Along with my colleague Andy Budd, I was named as one of <cite>Wired <abbr>UK</abbr></cite>’s top 100 digital power brokers.</p>

<h3>Colour Blindness</h3>
<p>I’m happy for people to know I’m colour blind – as a disability it proves quite useful in my work sometimes. Strictly speaking, I’m colour deficient: protanomaly, medically speaking. If you could turn the red down on your vision, you’d see how I see.</p>

<h3>Web typography evangelism</h3>
<p>I have a deep love and fascination for typography. I created Clearleft’s Fontdeck web service, in partnership with OmniTI and <a href="http://jontangerine.com/">Jon Tan</a>. As a self-appointed web typography evangelist, I am chief organiser of Clearleft’s <a href="http://ampersandconf.com/">Ampersand web typography conference</a>. I also put together <a href="http://webtypography.net/">webtypography.net</a> which translates Robert Bringhurst’s typographic guidelines into <abbr>CSS</abbr>. I have a web typography book in the works with Five Simple Steps, which may get finished one year soon.</p>
<p>I am a member of the <abbr title="Interaction Design Association">IxDA</abbr>, London <abbr title="Information Architecture">IA</abbr> group and <abbr title="Association Typographique International">ATypI</abbr>. I have spoken at <a href="http://lanyrd.com/profile/clagnut/past/speaking/">numerous conferences</a> on prototyping and typography, including An Event Apart, <abbr>UIE</abbr>’s Web App Summit and <abbr>UX</abbr> London.</p>

<h3>Away from the office</h3>

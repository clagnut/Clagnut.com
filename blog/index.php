<h3>Heading level 3</h3>

<p>The book is published in Penguin’s <a href="http://www.penguin.co.uk/static/cs/uk/0/aboutus/publishingstructure.html">Allen Lane range</a> and as such is finely typeset in <a href="http://store.adobe.com/type/browser/P/P_088.html" title="Adobe type library">Adobe Sabon</a> (a digital incarnation of Tschichold’s typeface). I mention this because the Penguin paperback novels are all too obviously machine-set nowadays, a sad loss introduced when Penguin’s type design department was disbanded in 2003.</p>


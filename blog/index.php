	<p>The fifth book I bought was <a href="http://www.amazon.co.uk/exec/obidos/ASIN/0713998393/jalfrezi-21/" title="More info on Amazon">Penguin by Design – a Cover Story</a> by Phil Baines. Penguin books have a rather illustrious heritage, with legendary typographer <a href="http://en.wikipedia.org/wiki/Jan_Tschichold" title="Wikipedia - Jan Tschichold">Jan Tschichold</a> responsible for the iconic orange spines, three-pane covers with <a href="http://www.identifont.com/show?MB" title="Identifont">Gill Sans</a>, and of course serious attention to detail in the typesetting.</p>

	<p>Phil Baines takes us through the seventy years of history in the
design of Penguin paperback covers, a constantly evolving part of
Britain’s culture and design history. The book is lavishly illustrated,
with many full size covers, and goes into detail of individual titles.
Fascinating too is the coverage of changing role of artists and
designers throughout Penguin’s history. It’s a luscious book for anyone
interested in typographical-related design. </p>

	<p>The book is published in Penguin’s <a href="http://www.penguin.co.uk/static/cs/uk/0/aboutus/publishingstructure.html">Allen Lane range</a> and as such is finely typeset in <a href="http://store.adobe.com/type/browser/P/P_088.html" title="Adobe type library">Adobe Sabon</a>
 (a digital incarnation of Tschichold’s typeface). I mention this
because the Penguin paperback novels are all too obviously machine-set
nowadays, a sad loss introduced when Penguin’s type design department
was disbanded in 2003.</p>

